function getSpaceHoldLanguage(language) {
  return language === "zh" ? "zh" : "en";
}

function getSpaceHoldMessages(language) {
  return SPACE_HOLD_I18N[getSpaceHoldLanguage(language)];
}

function spaceHoldTranslate(language, key, values = {}) {
  const messages = getSpaceHoldMessages(language);
  const text = messages[key] || SPACE_HOLD_I18N.en[key] || key;
  return Object.entries(values).reduce((result, [name, value]) => {
    return result.replaceAll(`{${name}}`, String(value));
  }, text);
}

function applySpaceHoldI18n(language, root = document) {
  const lang = getSpaceHoldLanguage(language);
  const messages = getSpaceHoldMessages(lang);
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

  root.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = messages[node.dataset.i18n] || SPACE_HOLD_I18N.en[node.dataset.i18n] || node.textContent;
  });

  root.querySelectorAll("[data-i18n-title]").forEach((node) => {
    node.title = messages[node.dataset.i18nTitle] || SPACE_HOLD_I18N.en[node.dataset.i18nTitle] || node.title;
  });

  root.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    node.setAttribute(
      "aria-label",
      messages[node.dataset.i18nAriaLabel] || SPACE_HOLD_I18N.en[node.dataset.i18nAriaLabel] || node.getAttribute("aria-label") || ""
    );
  });
}
